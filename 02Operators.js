/*****************Operators**********************/


// prompt() are strings 

var a = Number(prompt("Enter a number A"));
//Prompt() to work with inputs
var b = Number(prompt("Enter a number B"));

//****************Arithmatic Operators***************//
var sum = a+b;
console.log(`Sum is ${sum}`);

var sub = a-b;
console.log(`Sub is ${sub}`);

var mul = a*b;
console.log(`Mul is ${mul}`);

var div = a/b;
console.log(`Div is ${div}`);

var modulus = a%b;
console.log(`Modulus is ${modulus}`);

var exponential = a**b;
console.log(`Exponential is ${exponential}${typeof(b)}`);

var increment = a ++,b
console.log(`Incriment is ${increment},${typeof(b)}`);

var decrement = a --,b
console.log(`Decrement is ${decrement}`);


